package Dto;

public class LineDTO {
	private String data;
	private String idConsentimento;
	private String idPessoa;
	private String idMarca;
	private String idSegmento;
	private String log;
	private String excepition;
	
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getIdConsentimento() {
		return idConsentimento;
	}
	public void setIdConsentimento(String idConsentimento) {
		this.idConsentimento = idConsentimento;
	}
	public String getIdPessoa() {
		return idPessoa;
	}
	public void setIdPessoa(String idPessoa) {
		this.idPessoa = idPessoa;
	}
	public String getIdMarca() {
		return idMarca;
	}
	public void setIdMarca(String idMarca) {
		this.idMarca = idMarca;
	}
	public String getIdSegmento() {
		return idSegmento;
	}
	public void setIdSegmento(String idSegmento) {
		this.idSegmento = idSegmento;
	}
	public String getLog() {
		return log;
	}
	public void setLog(String log) {
		this.log = log;
	}
	public String getExcepition() {
		return excepition;
	}
	public void setExcepition(String excepition) {
		this.excepition = excepition;
	}

}
